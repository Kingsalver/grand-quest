public class Entity {
    private int HP;
    private int power;

    public Entity (){
        
    }








    public int getHP(){
        return HP;
    }

    public void setHP(int HP){
        this.HP = HP;
    }

    public int getPower(){
        return power;
    }

    public void setPower(int power){
        this.power = power;
    }


}
